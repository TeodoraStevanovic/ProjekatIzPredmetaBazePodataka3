     private void prepareView() throws Exception {
       pripremiComboRoba();
    }

    private void pripremiComboRoba() throws Exception {
        List<JedinicaMere> listaJm = Controller.getInstance().getAllJedinicaMere();
        cmbJM1.removeAllItems();
        for (JedinicaMere jm : listaJm) {
            cmbJM1.addItem(jm);
    }
    
     
    }

    void setRoba(Roba roba) {
       
        txtsifrarobe1.setText(String.valueOf(roba.getSifraRobe()));
        txtnazivRobe1.setText(roba.getNazivRobe());
        txtbarkod1.setText(String.valueOf(roba.getBarKod())); 
        cmbJM1.setSelectedItem(roba.getJedinicaMere());
    txtnazivJM1.setText(roba.getNazivJediniceMere());
    }

