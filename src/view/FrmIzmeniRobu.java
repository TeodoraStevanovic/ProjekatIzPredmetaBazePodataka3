    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
      
         try {
            Roba roba= new Roba();
            roba.setSifraRobe(Integer.parseInt(txtsifrarobe1.getText().trim()));
            roba.setNazivRobe(txtnazivRobe1.getText().trim());
            roba.setBarKod(Long.parseLong(txtbarkod1.getText().trim()));
            roba.setJedinicaMere((JedinicaMere) cmbJM1.getSelectedItem());
            Controller.getInstance().editRoba(roba);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio robu.", "Uspešno čuvanje robe", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (Exception ex) {
           
           JOptionPane.showMessageDialog(this,"Sistem ne može da zapamti robu","Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

