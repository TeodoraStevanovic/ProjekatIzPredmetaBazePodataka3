    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try {
            Validator.startValidation()
            .validateNotNullOrEmpty(txtnazivRobe.getText(), "naziv robe je obavezno polje")
            .validateNotNullOrEmpty(txtsifrarobe.getText(), "sifra robe je obavezno polje")
            .throwIfInvalide();
            
            Roba roba= new Roba();
            roba.setSifraRobe(Integer.parseInt(txtsifrarobe.getText().trim()));
            roba.setNazivRobe(txtnazivRobe.getText().trim());
            roba.setBarKod(Long.parseLong(txtbarkod.getText().trim()));
            roba.setJedinicaMere((JedinicaMere) cmbJM.getSelectedItem());
            Controller.getInstance().addRoba(roba);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio robu.", "Uspe≈°no ƒçuvanje robe", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Sistem ne mo≈æe da zapamti robu", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDodajActionPerformed

